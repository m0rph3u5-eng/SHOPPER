function age0to2() {
    // Get the checkbox
    var checkBox = document.getElementById("age0to2check");
    // Get the output text
    var text = document.getElementById("age0to2list");
    // If the checkbox is checked, display the output text
    if (checkBox.checked == true) {
      text.style.display = "block";
    } else {
      text.style.display = "none";
    }
  }

